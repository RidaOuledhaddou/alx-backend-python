#!/usr/bin/env python3
"""
This module contains a function to join two strings together.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the combined result.

    Parameters:
    str1 (str): The first input string.
    str2 (str): The second input string.

    Returns:
    str: The combined string resulting from concatenating str1 and str2.
    """
    return str1 + str2

